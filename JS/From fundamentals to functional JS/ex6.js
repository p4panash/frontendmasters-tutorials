const _ = {}

_.filter = function(arr, cb) {
    result = []
    for (let i = 0; i < arr.lenght; i++) {
        if (cb(arr[i], i, arr) === true)
            result.push(arr[i])
    }
    return result;
}

_.filter = function(arr, cb) {
    result = []
    ._each(arr, function(item, i, list) {
        if (cb(item, i, list) === true)
            result.push(item);
    })
    return result;
}

